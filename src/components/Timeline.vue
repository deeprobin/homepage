<script>
export default {
    name: 'Timeline',
    created: function() {
        window.addEventListener("load", this.updateTimeline);
        window.addEventListener("resize", this.updateTimeline);
        window.addEventListener("scroll", this.updateTimeline);
    },
    destroyed: function() {
        window.removeEventListener("load", this.updateTimeline);
        window.removeEventListener("resize", this.updateTimeline);
        window.removeEventListener("scroll", this.updateTimeline);
    },
    methods: {
        isElementInViewport: function(el) {
            const rect = el.getBoundingClientRect();
            return (
                rect.top >= 0 &&
                rect.left >= 0 &&
                rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) &&
                rect.right <= (window.innerWidth || document.documentElement.clientWidth)
            );
        },
        updateTimeline: function() {
            const items = this.$el.querySelector(".timeline li");
            for(let i = 0; i < items.length; i++) {
                if(this.isElementInViewport(items[i])) {
                    items[i].classList.add("in-view");
                }
            }
        }
    }
}
</script>